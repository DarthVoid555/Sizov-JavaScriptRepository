function Сотрудник(имя, отдел, телефон, зарплата) {
    this.имя = имя;
    this.отдел = отдел;
    this.телефон = телефон;
    this.зарплата = зарплата;
    
    this.отобразить = function() {
        console.log("Данные сотрудника:");
        console.log("Имя: " + this.имя);
        console.log("Отдел: " + this.отдел);
        console.log("Телефон: " + this.телефон);
        console.log("Зарплата: " + this.зарплата + " руб.");
    };
}

let сотрудник1 = new Сотрудник("Иван Петров", "IT-отдел", "+7 (999) 123-45-67", 75000);

сотрудник1.отобразить();

console.log("\nИмя сотрудника: " + сотрудник1.имя);